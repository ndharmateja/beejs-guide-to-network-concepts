#   2342 =   0b..000_1001_0010_0110
# & 2332 = & 0b..000_1001_0001_1100
# ----------------------------------
#   2308 =   0b..000_1001_0000_0100
# ----------------------------------
print(2342 & 2332)

#   0b..000_0011_0101
# | 0b..000_0111_0000 = 112
# --------------------------
#   0b..000_0111_0101 = 117
# --------------------------
print(0b110101 | 112)  # 117

# (~0b..000_1010_1001_0101) = 0b..111_0101_0110_1010 = -2710
print(~0b101010010101)
# (~0b..000_1010_1001_0101) & 0b..000_1111_1111_1111 = 0b..000_0101_0110_1010 = 1386
print(~0b1010_1001_0101 & 0b1111_1111_1111)

# Left shifts multiply by 2 and right shifts divide by 2
print(16 << 1)  # 32
print(64 << 1)  # 128
print(4200 << 1)  # 8400
print(16 >> 2)  # 4

# 0b..0000_1110_0111 = 231
# 0b..0000_1110_0111 << 3 = 0b..0111_0011_1000 = 231 * 8 = 1848
print(0b1110_0111 << 3)

# 0b1111_1111 = 255
print((1 << 8) - 1)

# 0x01020304 & 0b..000_1111_1111_1111_1111
# = 0b..0000_0001_0000_0010_0000_0011_0000_0100 & 0b..000_1111_1111_1111_1111
# = 0b..000_0000_0011_0000_0100
# = 0x0304 = 772
print(0x01020304 & ((1 << 16) - 1))
